package Lab3;

public class func3_1 {
  public static double fahrenheit (double f){
      return ((f/(9.0/5.0))-32.0);
  }
  public static double celsius (double c){
      return (c*(9.0/5.0)+32.0);
  }
  public double i_fahrenheit (double f){
      return ((f/(9.0/5.0))-32.0);
  }
  public static double i_celsius (double c){
      return (c*(9.0/5.0)+32.0);
  }
}